package example.day02._2REST;

public class TaskDto {
   private String name;
   private int age;

   public TaskDto() {
   }

   public String getName() {
      return name;
   }

   public void setName(String name) {
      this.name = name;
   }

   public int getAge() {
      return age;
   }

   public void setAge(int age) {
      this.age = age;
   }
}
